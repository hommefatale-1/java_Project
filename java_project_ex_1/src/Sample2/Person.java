package Sample2;

public class Person {
	private String namel;
	private int age;
	private int phone;

	Person(String namel, int age) {
		this.namel = namel;
		this.age = age;
	}

	public String getNamel(String string) {
		return namel;
	}

	public void setNamel(String namel) {
		this.namel = namel;
	}

	public int getAge(int age) {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getPhone(int phone) {
		return phone;
	}

	public void setPhone(int phone) {
		this.phone = phone;
	}

}
